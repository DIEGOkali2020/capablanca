#include <iostream>

using namespace std;

int main() {
	
	int n = 0 , count = 0, largest;
	
	while(count <=10){
		
		cout<<"\nDigite um numero qualquer: ";
		cin>>n;
		
		if(count == 0){
			
			largest = n;
			
			}else{
							   
				if(n > largest){
					
					largest = n;
					}
				}
		count++;
		}
	
	cout<<"O maior numero e : "<<largest<<endl;
	
	return 0;
}
